const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "7c2397fc";

export default function App() {

  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState("");
  const {movies, isLoading, error} = useMovies(query, hundleCloseMovie)
  const [watched, setWatched] = useLocalStorageState([], "watched")

  function hundleSelectMovie(id) {
    setSelectedId((selectedId) => (selectedId === id ? null : id));
  }

  function hundleCloseMovie() {
    setSelectedId(null);
  }

  function hundleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function hundleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }


  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <Numresult movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />} */}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={hundleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>

        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={hundleCloseMovie}
              onAddWatched={hundleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={hundleDeleteWatched}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

